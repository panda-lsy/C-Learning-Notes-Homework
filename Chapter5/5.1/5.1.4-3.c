#include "stdio.h"

int main(){
    int i,j,key,n;
    scanf("%d",&n);   // 读入数组长度n
    int a[n];         // 定义长度为n的数组
    
    // 输入数组元素
    for (int i=0;i<n;i++){
        scanf("%d",&a[i]);
    }
    
    // 插入排序算法实现
    // 基本原理：将数组分为已排序和未排序两部分，每次从未排序部分取出一个元素
    // 插入到已排序部分的正确位置
    
    // 从第二个元素开始遍历(第一个元素视为已排序)
    for (i = 1;i<n;i++){
        key = a[i];   // 保存当前要插入的元素
        j=i-1;        // j指向已排序部分的最后一个元素
        
        // 在已排序部分从右向左查找插入位置
        // 如果已排序部分的元素大于key，则将该元素右移一位
        while (j>=0 && a[j]>key){
            a[j+1]=a[j];  // 元素后移，为key腾出位置
            j--;          // 继续向左比较
        }
        
        a[j+1]=key;   // 找到正确位置，将key插入
    }
    
    // 输出排序后的数组
    for (int i=0;i<n;i++){
        printf("%d ",a[i]);
    }
    return 0;
}